//Primitive data types in JavaScript

// Number
let age = 24;
console.log(age);

//string
let name = "tasfia";
console.log(name);

//boolean
let isFollow = true;
console.log(name + " is following: " + isFollow);

//undefined
let x;
console.log(x);

//null
let y = null;
console.log(y);

//symbol
let sym = Symbol("unique");
console.log(sym);
// BigInt
let bigNumber = BigInt(1234);
console.log(bigNumber);